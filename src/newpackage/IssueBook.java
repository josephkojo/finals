  private boolean issueBook() throws SQLException {
   
    int bookId = Integer.parseInt(txtBookId.getText());
    String bookTitle = titleBook.getText();
    String studentId = txtIndex.getText();
    String studentName = name.getText();
    Date issueDate = (Date) rSDateChooser1.getDatoFecha();
    Date dueDate = (Date) rSDateChooser2.getDatoFecha();
    long getFirstDate = issueDate.getTime();
    long getSecondDate = dueDate.getTime();
    java.sql.Date date1 = new java.sql.Date(getFirstDate);
    java.sql.Date date2 = new java.sql.Date(getSecondDate);
    Connection connection = DbConnection.getConnection();
    String insertData = "INSERT INTO issueBooks VALUES (?,?,?,?,?,?,?)";
    PreparedStatement statement = connection.prepareStatement(insertData);
    statement.setInt(1, bookId);
    statement.setString(2, bookTitle);
    statement.setString(3, studentId);
    statement.setString(4, studentName);
    statement.setDate(5, date1);
    statement.setDate(6, date2);
    statement.setString(7, "Pending");

    int result = statement.executeUpdate();
    return result > 0;

}

