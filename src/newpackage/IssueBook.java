    private void getBookDetails() throws SQLException{
        int bookId = Integer.parseInt(txtBookId.getText());
        
        Connection connection = DbConnection.getConnection();
        String query = "SELECT * FROM book_details WHERE book_id = ? ";
        PreparedStatement statement = connection.prepareStatement(query);
        statement.setInt(1, bookId);
        ResultSet resultSet = statement.executeQuery();
        while(resultSet.next()){
            bookName.setText(resultSet.getString("book_id"));
            titleBook.setText(resultSet.getString("book_name"));
            author.setText(resultSet.getString("author"));
            quantity.setText(resultSet.getString("Quantity"));
            
            
            
        }
    }
    private void getStudentDetails() throws SQLException{
        String studentId = txtIndex.getText();
        Connection  connection = DbConnection.getConnection();
        String query = "SELECT * FROM studenttable WHERE Student_Id = ?";
        PreparedStatement statement = connection.prepareCall(query);
        statement.setString(1, studentId);
        ResultSet result = statement.executeQuery();
        if(result.next()){
            txtIndex.setText(result.getString("Student_Id"));
            name.setText(result.getString("Student_Name"));
            field.setText(result.getString("Programme"));
            prog.setText(result.getString("datafield"));
          
        }else{
            JOptionPane.showMessageDialog(this, studentId + " is not in our database");
        }
    }
