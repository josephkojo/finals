    private Boolean addStudent() throws SQLException{
        boolean isAdded = false;
        String studentId = studentID.getText();
        String studentName = name.getText();
        String checkField = field1.getSelectedItem().toString();
        String checkField2 = field2.getSelectedItem().toString();
        
        
        Connection connection  = DbConnection.getConnection();
        String query = "INSERT INTO studenttable values (?,?,?,?)";
        PreparedStatement statement = connection.prepareStatement(query);
        statement.setString(1, studentId);
        statement.setString(2, studentName);
        statement.setString(3, checkField);
        statement.setString(4, checkField2);
        
        
        int inserted = statement.executeUpdate();
        isAdded = inserted > 0;
        return isAdded;
        
        
    }
