    private void saveTableData() throws SQLException{
        Connection con = DbConnection.getConnection();
        Statement statement = con.createStatement();
       ResultSet resultSet = statement.executeQuery("SELECT * FROM studenttable");
       while(resultSet.next()){
           String indexNumeber = resultSet.getString("Student_Id");
           String studentName = resultSet.getString("Student_Name");
           String field = resultSet.getString("Programme");
           String programme = resultSet.getString("datafield");
           Object [] details = {indexNumeber, studentName, field, programme};
           student = (DefaultTableModel) studentTable.getModel();
           student.addRow(details);
           
       }
       
        
    }
    public void clearTable(){
        DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
        model.setRowCount(0);
        
    }
    private boolean upDateBook() throws SQLException{
        boolean isUpdated = false;
        String indexNumber  = studentID.getText();
        String names =  name.getText();
        String field = field1.getSelectedItem().toString();
        String fields = field2.getSelectedItem().toString() ;
        Connection con = DbConnection.getConnection();
        String Statements = "UPDATE studenttable SET Student_Name = ?, Programme = ?, datafield = ? WHERE Student_Id = ? ";
        PreparedStatement statement = con.prepareStatement(Statements);
       
        statement.setString(1, names);
        statement.setString(2, field);
        statement.setString(3, fields);
         statement.setString(4, indexNumber);
       int inserted = statement.executeUpdate();
       if(inserted > 0){
           isUpdated = true;
       }else{
           isUpdated = false;
       }
       return isUpdated;
                
        
    }
    public boolean deleteBook() throws SQLException{
        boolean isdeleted = false;
        String indexNumber = studentID.getText();
        Connection connection = DbConnection.getConnection();
        String Query = "DELETE FROM studenttable WHERE book_id = ?";
        PreparedStatement statement =  connection.prepareStatement(Query);
        statement.setString(1, indexNumber);
        int result = statement.executeUpdate();
        if(result > 0){
            isdeleted = true;
        }else{
            isdeleted = false;
        }
        return isdeleted;
        
    }
