    private void upDateBookCount() throws SQLException{
        int bookId = Integer.parseInt(getBookId.getText());
        
        Connection connection = DbConnection.getConnection();
        String query = "UPDATE book_details SET Quantity = Quantity -1 WHERE book_id = ?";
        
        PreparedStatement statement = connection.prepareStatement(query);
        statement.setInt(1, bookId);
        int ResultSet  = statement.executeUpdate();
        if(ResultSet > 0){
            int converted = Integer.parseInt(txtQuantity.getText());
        int display = converted - 1;
        String value = Integer.toString(display);
            txtQuantity.setText(value);
            
        }
        
        
    }
