    private void displayBook() throws SQLException{
        int BookId = Integer.parseInt(getBookId.getText());
        Connection connection = DbConnection.getConnection();
        String query = "SELECT * FROM book_details WHERE book_id = ?";
        PreparedStatement statement = connection.prepareStatement(query);
        statement.setInt(1, BookId);
        ResultSet set = statement.executeQuery();
        if(set.next()){
            getIdBook.setText(set.getString("book_id"));
            bookName.setText(set.getString("book_name"));
            txtAuthor.setText(set.getString("author"));
            txtQuantity.setText(set.getString("Quantity"));
        }else{
            JOptionPane.showMessageDialog(this, "The book id does not exist");
        }
        
        
    }
    
